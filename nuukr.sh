main() {
encshr=$((RANDOM%((500-200)+1)+200))
ascilogo
echo $'\e[1;31m---------------------------------------\e[0m'
echo $'\e[1;31m!Include File Path if not in directory!\e[0m'
echo $'\e[1;31m---------------------------------------\e[0m'
echo ""
read -p $'\e[1;36mDir/File?: \e[0m' path
if [ -f "$path" ]
then
  sudo shred -u -n $encshr "$path"
elif [ -d "$path" ]; then
  sudo rm -rf "$path"
else
  echo "Invalid path!"
  main
fi
}
main
